module.exports = class Person {
	constructor(
		fname,
		mname,
		lname,
		dob,
		phone,
		email,
		street,
		city,
		state,
		zip
	) {
		this.fname = fname;
		this.mname = mname;
		this.lname = lname;
		this.dob = dob;
		this.phone = phone;
		this.email = email;
		this.street = street;
		this.city = city;
		this.state = state;
		this.zip = zip;
	}
};




